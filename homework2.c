// write a program to check if given character is digit or not.
int main(){
    int a;
    printf("enter character :");
    scanf("%d",&a);
    if(a>=0||a<0){
        printf("digit \n");
    }
    else{
        printf("not a digit \n");
    }
    printf("thank you \n");
    return 0;
}
    